package Operators;

public class IncDec {
	 public static void main(String[] args) {
	       
		 int varX = 5;
	   	 int varY = 5;

	      
	       
	        System.out.println(++varX);

	      
	        System.out.println(--varY);
	        

	       
	        System.out.println(varX--);

	        
	        System.out.println(varY--);
	    }

}
