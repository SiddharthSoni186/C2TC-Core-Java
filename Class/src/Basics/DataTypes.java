package Basics;


public class DataTypes {

	public static void main(String[] args) {
		
		int myint = 16;                 
		boolean mybool = true;       
		char mychar = 'S';           
		float myfloat = 14.9f;             
		double myNum = 19.99d;         
		
		
		System.out.println(myint);
		System.out.println(mybool);
		System.out.println(mychar);
		System.out.println(myfloat);
		System.out.println(myNum);
		
		
	}
}
